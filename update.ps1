$ErrorActionPreference = 'Stop'

# Update styles.css
Write-Host 'Updating styles.css...'
$css = Get-Content -Path 'styles.css' -Raw -Encoding UTF8
$css = $css -replace '--bg-root:\s*#0a0e1a;', '--bg-root:          #0D1425;'
$css = $css -replace '--bg-control-panel:\s*#080c16;', '--bg-control-panel: #0D1425;'
